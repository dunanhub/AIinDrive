## –°—Ç–∞—Ä–∞—è —Ñ–æ—Ä–º—É–ª–∞: (–ß–∏—Å—Ç–æ—Ç–∞ + –¶–µ–ª–æ—Å—Ç–Ω–æ—Å—Ç—å) / 2
## –ù–æ–≤–∞—è —Ñ–æ—Ä–º—É–ª–∞: –ß–∏—Å—Ç–æ—Ç–∞√ó30% + –¶–µ–ª–æ—Å—Ç–Ω–æ—Å—Ç—å√ó70%

def old_formula(clean, integrity):
    return (clean + integrity) / 2

def new_formula(clean, integrity):
    return (clean * 0.3) + (integrity * 0.7)

# –ü—Ä–∏–º–µ—Ä—ã —Å—Ä–∞–≤–Ω–µ–Ω–∏—è
test_cases = [
    (90, 50),  # –ß–∏—Å—Ç–∞—è, –Ω–æ –ø–æ–≤—Ä–µ–∂–¥–µ–Ω–Ω–∞—è
    (50, 90),  # –ì—Ä—è–∑–Ω–∞—è, –Ω–æ —Ü–µ–ª–∞—è
    (80, 80),  # –°—Ä–µ–¥–Ω–µ –∏ —Ç–æ –∏ —Ç–æ
    (100, 100), # –ò–¥–µ–∞–ª—å–Ω–∞—è
    (30, 30),   # –ü–ª–æ—Ö–∞—è
]

print("| –ß–∏—Å—Ç–æ—Ç–∞ | –¶–µ–ª–æ—Å—Ç–Ω–æ—Å—Ç—å | –°—Ç–∞—Ä–∞—è —Ñ–æ—Ä–º—É–ª–∞ | –ù–æ–≤–∞—è —Ñ–æ—Ä–º—É–ª–∞ | –†–∞–∑–Ω–∏—Ü–∞ |")
print("|---------|-------------|----------------|---------------|---------|")

for clean, integrity in test_cases:
    old = old_formula(clean, integrity)
    new = new_formula(clean, integrity)
    diff = new - old
    print(f"| {clean:7}% | {integrity:11}% | {old:14.1f}% | {new:13.1f}% | {diff:+6.1f}% |")

